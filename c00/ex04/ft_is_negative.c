#include <unistd.h>

void	ft_is_negative(int num)
{
	if (num < 0)
	{
		write(1, "N", 1);
	}
	if (num >= 0)
	{
		write(1, "P", 1);
	}
}

int	main(void)
{
	ft_is_negative(0);
}
